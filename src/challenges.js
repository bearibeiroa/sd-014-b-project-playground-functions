function highestCount(arrays) {
  let maxNumber = Math.max(...arrays);
  let result = 0;
  for (let index = 0; index < arrays.length; index += 1) {
    if (arrays[index] == maxNumber) {
      result += 1;
    }
  }
  return result;
}

